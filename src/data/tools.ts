export type Provenance = "independently_verified" | "vendor_claimed" | "community_reported";

export interface PricingPlan {
  name: string;
  priceMonthly: number | null; // null = custom/contact
  priceAnnual: number | null;
  billingModel: "per_seat" | "flat" | "usage" | "custom";
  limits: string;
  highlighted?: boolean;
}

export interface ToolScore {
  value: number;       // 1-10: bang for buck
  ease: number;        // 1-10: learning curve / UX
  power: number;       // 1-10: feature depth
  setupFriction: number; // 1-10 (10 = easiest)
  migrationDifficulty: number; // 1-10 (10 = easiest to switch to)
  transparency: number; // 1-10: pricing clarity, data export, API openness
}

export interface Tool {
  slug: string;
  name: string;
  vendor: string;
  website: string;
  tagline: string;
  description: string;
  categorySlug: string;
  categories: string[]; // can be in multiple
  scores: ToolScore;
  pricing: PricingPlan[];
  pricingUrl: string;
  freeTier: boolean;
  freeTierLimits?: string;
  bestFor: string[];
  avoidIf: string[];
  features: string[];
  integrations: string[];
  platforms: string[];
  deployment: string; // "cloud" | "self-hosted" | "hybrid"
  compliance: string[];
  dataExport: string;
  apiAvailable: boolean;
  provenanceLevel: Provenance;
  lastVerified: string; // ISO date
  lastTested?: string;
  affiliateUrl: string;
  badge?: string;
  switchingTriggers?: string[];
  switchingBlockers?: string[];
  implementationComplexity: "low" | "medium" | "high";
  alternatives: string[]; // slugs
  changelogUrl?: string;
}

